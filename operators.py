#arithmetic operators - simple calculations
num1= 10
num2 =3

print(num1+num2)
print(num1-num2)
print(num1/num2)
print(num1*num2)
print(num1%num2)  #modulus - returns the remainder after division

#comparison operators - used to compare values
print(num1 < num2)
print(num1 > num2)
print(num1 <= num2)
print(num1 >= num2)
print(num1 == num2)  #equal to
print(num1 != num2) #not equal to

#assignment operators -used to assign values to variables
x=100
print(x)


x += 2
print(x)

Y= 56
Y -= 3
print(Y)

#logic operators  - and ,or , not
a= 23
print(a  < 40  and a <2) # if both are true returns a true but if one is false  brings a false
print(a  < 40  or a <2)
print(not(a  < 40  or a <2))


